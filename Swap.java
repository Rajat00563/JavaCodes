//Wap to swap two numbers with and without using 3rd varibles.
class Swap{
    public static void main(String args[]){
        int a = 5, b = 10;
        a = a +b;
        b = a -b;
        a = a -b;
        System.out.println("Value of a is" + a);
        System.out.println("Value of b is" + b);

    }
}